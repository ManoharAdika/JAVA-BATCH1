package batch1java.oopsexamples.conditionalStates;

public class SwitchConditionalExamples {

	public static void main(String[] args) {
		
		String day ="jan";
		
		switch(day) {
		case "jan":
			System.out.println("jan");
		break;
		case "2":
			System.out.println("feb");
		break;
		case "3":
			System.out.println("mar");
		break;
		default:
			System.out.println("default.........");
		
		}
		
	}
}
